type I18nContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string, params?: TranslationParams) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

function detectLocale(): Locale {
  if (typeof navigator === "undefined") return "fr";

  const languages = navigator.languages?.length ? navigator.languages : [navigator.language];
  const matchedLocale = languages
    .map((language) => language.toLowerCase().split("-")[0])
    .find((language): language is Locale => SUPPORTED_LOCALES.includes(language as Locale));

  return matchedLocale ?? "fr";
}

function translate(locale: Locale, key: string, params: TranslationParams = {}) {
  const template = String(
    translations[locale][key as keyof typeof translations.fr] ??
    translations.fr[key as keyof typeof translations.fr] ??
    key
  );
  return Object.entries(params).reduce(
    (text, [name, value]) => text.split(`{${name}}`).join(String(value)),
    template
  );
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>(() => detectLocale());
  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale,
      t: (key, params) => translate(locale, key, params)
    }),
    [locale]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return context;
}
